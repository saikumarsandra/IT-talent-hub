
/*

++  ->  icrement operator -> icrement by 1
--  ->  decrement operator -> decrement by 1 

i=1;


prefix  -> ++i ->  returns the valur after the increment/ decrement  
postfix -> i++ -> returns the value before the increment/ decrement  

*/


let x = 10;
x++;
console.log(x);

x--;
console.log(x);


let y = 3;
++y;
console.log(y);
--y;
console.log(y);



let z = 7;
console.log(z++); // 
console.log(z);

let i = 5;
console.log(++i);
